package br.com.cicom.comunicacicom.DSPrimary.DTO.seguranca;

public class GrupoDTO implements Comparable<GrupoDTO> {
	private Long id;
	private String nome;

	public GrupoDTO() {
	}

	public GrupoDTO(Long id, String nome) {
		this.id = id;
		this.nome = nome;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		result = prime * result + ((nome == null) ? 0 : nome.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		GrupoDTO other = (GrupoDTO) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		if (nome == null) {
			if (other.nome != null)
				return false;
		} else if (!nome.equals(other.nome))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return "GrupoDTO [id=" + id + ", nome=" + nome + "]";
	}

	@Override
	public int compareTo(GrupoDTO grupo) {
		return this.getNome().toUpperCase().compareTo(grupo.getNome().toUpperCase());
	}
}
